def translate_text(text: str) -> str:
    """Translate Chinese to English using dictionary."""
    # Sort by length to match longer phrases first
    sorted_trans = sorted(TRANSLATIONS.items(), key=lambda x: len(x[0]), reverse=True)
    
    result = text
    for chinese, english in sorted_trans:
        result = result.replace(chinese, english)
    
    return result

def process_file(file_path: Path) -> bool:
    """Process a single file."""
    try:
        content = file_path.read_text(encoding='utf-8')
        original = content
        
        # Translate everything with Chinese
        if re.search(r'[\u4e00-\u9fff]', content):
            content = translate_text(content)
        
        if content != original:
            file_path.write_text(content, encoding='utf-8')
            return True
        return False
        
    except Exception as e:
        print(f"Error processing {file_path}: {e}")
        return False

def main():
    """Main process."""
    base_path = Path(__file__).parent
    
    all_php = list(base_path.rglob('*.php'))
    php_files = [f for f in all_php if 'zh_CN' not in str(f)]
    
    print(f"Processing {len(php_files)} PHP files...")
    print("=" * 60)
    
    modified = 0
    for i, file_path in enumerate(php_files, 1):
        if i % 50 == 0:
            print(f"Progress: {i}/{len(php_files)}")
        
        if process_file(file_path):
            modified += 1
    
    print("=" * 60)
    print(f"Complete! Modified {modified} files")
    
    # Count remaining
    total_chinese = 0
    for file_path in php_files:
        try:
            content = file_path.read_text(encoding='utf-8')
            chinese_count = len(re.findall(r'[\u4e00-\u9fff]+', content))
            total_chinese += chinese_count
        except:
            pass
    
    print(f"Remaining Chinese strings: {total_chinese}")

if __name__ == '__main__':
    main()
